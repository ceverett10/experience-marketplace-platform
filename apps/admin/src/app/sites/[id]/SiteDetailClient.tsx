'use client';

import React, { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { Card, CardContent } from '@experience-marketplace/ui-components';

interface Brand {
  id: string;
  name: string;
  tagline: string | null;
  primaryColor: string;
  secondaryColor: string;
  accentColor: string;
  headingFont: string;
  bodyFont: string;
  logoUrl: string | null;
  isAutoGenerated: boolean;
}

// Brand identity data stored in seoConfig
interface ToneOfVoice {
  personality?: string[];
  writingStyle?: string;
  doList?: string[];
  dontList?: string[];
}

interface TrustSignals {
  expertise?: string[];
  certifications?: string[];
  yearsFounded?: number;
  valuePropositions?: string[];
  guarantees?: string[];
}

interface BrandStory {
  mission?: string;
  vision?: string;
  values?: string[];
  targetAudience?: string;
  uniqueSellingPoints?: string[];
}

interface ContentGuidelines {
  keyThemes?: string[];
  contentPillars?: string[];
  semanticKeywords?: string[];
}

interface SeoConfig {
  toneOfVoice?: ToneOfVoice;
  trustSignals?: TrustSignals;
  brandStory?: BrandStory;
  contentGuidelines?: ContentGuidelines;
}

interface Domain {
  id: string;
  domain: string;
  status: string;
  verifiedAt: string | null;
  sslEnabled: boolean;
}

interface Page {
  id: string;
  title: string;
  slug: string;
  type: string;
  status: string;
}

interface Job {
  id: string;
  type: string;
  status: 'PENDING' | 'SCHEDULED' | 'RUNNING' | 'COMPLETED' | 'FAILED' | 'CANCELLED' | 'RETRYING';
  priority: number;
  attempts: number;
  maxAttempts: number;
  error: string | null;
  result: any;
  scheduledFor: string | null;
  startedAt: string | null;
  completedAt: string | null;
  createdAt: string;
}

interface Site {
  id: string;
  name: string;
  slug: string;
  description: string | null;
  status: string;
  isAutomatic: boolean;
  autonomousProcessesPaused: boolean;
  pauseReason: string | null;
  primaryDomain: string | null;
  suggestedDomain: string;
  gscVerified: boolean;
  gscVerifiedAt: string | null;
  seoConfig: SeoConfig | null;
  createdAt: string;
  updatedAt: string;
  publishedAt: string | null;
  brand: Brand | null;
  domains: Domain[];
  pages: Page[];
  counts: {
    pages: number;
    content: number;
    bookings: number;
  };
}

interface JobSummary {
  total: number;
  pending: number;
  running: number;
  completed: number;
  failed: number;
}

interface RoadmapTask {
  type: string;
  label: string;
  description: string;
  status: 'PLANNED' | 'PENDING' | 'SCHEDULED' | 'RUNNING' | 'COMPLETED' | 'FAILED';
  job: {
    id: string;
    status: string;
    startedAt: string | null;
    completedAt: string | null;
    error: string | null;
    attempts: number;
  } | null;
}

interface RoadmapPhase {
  key: string;
  name: string;
  description: string;
  status: 'pending' | 'in_progress' | 'completed' | 'failed';
  progress: {
    completed: number;
    total: number;
    percentage: number;
  };
  tasks: RoadmapTask[];
}

interface Roadmap {
  siteId: string;
  phases: RoadmapPhase[];
  overall: {
    completed: number;
    total: number;
    percentage: number;
  };
}

// Human-friendly job type labels
